number1 = int(input('Enter a number: '))
number2 = int(input('Enter a number: '))
user_action = input('(+ | - | * | / ) : ')
if user_action == "+":
    print(number1 + number2)
elif user_action == "-":
    print(number1 - number2)
elif user_action == "*":
    print(number1 * number2)
elif user_action == "/":
    print(number1 / number2)
else:
    print("you selected wrong operator!!!")

